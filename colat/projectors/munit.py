class MunitProjector(Projector):
    def __init__(
        self,
        layers: int,
        load_path: str ,
        normalize: bool = True,
        
    ):
        # nonlinear mlp
        self.layers=layers
        
        #using default params
        net = ContentEncoder(2,4,3,64,'in','relu',pad_type='reflect')
        munit_dict = torch.load(load_path)['a']
        enc_dict = {}
        for k, v in munit_dict.items():
            if 'enc_content.' in k:
                nk = k[12:]
                enc_dict[nk] = v

        net.load_state_dict(enc_dict)
        super().__init__(net, normalize)
        self.hidden_size = 99999

    def forward(self, input: torch.Tensor) -> torch.Tensor:
        out = self.net(input)
        out = F.max_pool2d(out,kernel_size=4)
        out = torch.flatten(out, 1)
        if self.normalize:
            norm = torch.norm(out, dim=1)
            return out / torch.reshape(norm, (-1, 1))
        return out


